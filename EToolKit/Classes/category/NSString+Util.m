        if ([self containsString:@"url="]) {
            NSRange targetRange = [self rangeOfString:@"url="];
            if (targetRange.location > 0) {
                NSRange urlRange = NSMakeRange((targetRange.location+targetRange.length), self.length-targetRange.location-targetRange.length);
                NSString *directUrl = [self substringWithRange:urlRange];
                NSDictionary *params = @{
                                         @"url":directUrl
                                         };
                return params;
            }
