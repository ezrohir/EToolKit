        }else{
            NSArray *sepratedStrings = [self componentsSeparatedByString:@"?"];
            if (sepratedStrings.count >1 ) {
                NSString *paramsString = [sepratedStrings lastObject];
                NSString *baseurl = nil;
                for (NSInteger i = 0 ; i< (sepratedStrings.count - 1); i++) {
                    NSString *str = [sepratedStrings objectAtIndex:i];
                    if (!baseurl) {
                        if (sepratedStrings.count == 2) {
                            baseurl = str;
                        }else{
                            baseurl = [NSString stringWithFormat:@"%@?",str];
                        }
                    }else{
                        baseurl = [NSString stringWithFormat:@"%@%@",baseurl,str];
                    }
                }
                params = [[NSMutableDictionary alloc] init];
                for (NSString *param in [paramsString componentsSeparatedByString:@"&"]) {
                    NSArray *elts = [param componentsSeparatedByString:@"="];
                    if([elts count] < 2) continue;
                    [params setObject:[elts lastObject] forKey:[elts firstObject]];
                }
                if (baseurl) {
                    [params setObject:baseurl forKey:@"baseurl"];
                }
                return params;
            }
        }
    }
    return params;
}


- (NSString *)md5Encrypt {
    const char *original_str = [self UTF8String];
    unsigned char result[CC_MD5_DIGEST_LENGTH];
    CC_MD5(original_str, (CC_LONG)strlen(original_str), result);
    NSMutableString *hash = [NSMutableString string];
    for (int i = 0; i < 16; i++)
        [hash appendFormat:@"%02X", result[i]];
    return [hash lowercaseString];
}

