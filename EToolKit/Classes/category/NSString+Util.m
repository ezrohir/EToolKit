        return self;
    }
    NSRange range = [self rangeOfComposedCharacterSequencesForRange:NSMakeRange(0, length - 1)];
    NSString *returnStr = [self substringWithRange:range];
    return [returnStr stringByAppendingString:@"..."];
}

